<template>
    <div>
        <div class="comments-wrap">
            <div id="comments" class="row">
                <div class="col-full">
                    <h3 class="h2">Comment</h3>
                    <ol class="commentlist">
                        <li class="depth-1 comment" v-for="comment in comments">
                            <div class="comment__avatar">
                                <v-gravatar class="avatar" :email="comment.user.email" width="50" height="50"/>
                            </div>
                            <div class="comment__content">
                                <div class="comment__info">
                                    <div class="comment__author">{{ comment.user.name }}</div>
                                    <div class="comment__meta">
                                        <div class="comment__time">{{ comment.created_at }}</div>
                                    </div>
                                </div>
                                <div class="comment__text"><p>{{ comment.body }}</p>
                                </div>
                            </div>
                        </li>
                    </ol>
                </div>
            </div>
            <div class="row comment-respond">
                <div id="respond" class="comments">
                    <h3 class="h2">Add Comment<span>Your email address will not be published</span></h3>
                    <form id="contactForm">
                        <fieldset>
                            <div class="message form-field">
                                <textarea ref="body" id="cMessage" class="full-width" placeholder="Your Message*"></textarea>
                            </div>
                            <google-re-captcha-v3
                                    ref="captcha" v-model="form.gRecaptchaResponse"
                                    :siteKey="this.siteKey"
                                    :id="'comments_id'"
                                    :inline="true"
                                    :action="'comments'"
                                    style="display: none">
                            </google-re-captcha-v3>
                            <p>This site is protected by reCAPTCHA and the Google
                                <a href="https://policies.google.com/privacy" target="_blank">Privacy Policy</a> and
                                <a href="https://policies.google.com/terms" target="_blank">Terms of Service</a> apply.</p>
                            <button id="submit" type="submit" @click.prevent="addComment" class="btn btn--primary btn-wide btn--large full-width">Add Comment</button>
                        </fieldset>
                    </form>
                </div>
            </div>
        </div>
    </div>
</template>


<script>
    import GoogleReCaptchaV3 from './googlerecaptchav3/GoogleReCaptchaV3';

    export default {
        props: {
            user: {
                type: Object,
                required: true
            },
            articleId: {
                type: Number,
                required: true
            }
        },
        components: {
            GoogleReCaptchaV3
        },
        data() {
            return {
                comments: [],
                form: {
                    gRecaptchaResponse: null
                },
                siteKey: process.env.MIX_CAPTCHA_SITE_KEY,
            };
        },
        created() {
            this.fetchComments();
            Echo.private("comment").listen("CommentSent", e => {
                this.comments.push({
                    user: {
                        name: e.user.name,
                        email: e.user.email
                    },
                    body: e.comment.body,
                    created_at: e.comment.created_at,
                });
            });
        },
        methods: {
            fetchComments() {
                axios.get("/api/v1/comments/" + this.articleId).then(response => {
                    this.comments = response.data;
                });
            },
            addComment() {
                console.log(this.$refs.captcha.execute());
                let body = this.$refs.body.value;
                axios.post("/" + this.articleId + "/comments", {body}).then(response => {
                    this.comments.push({
                        user: {
                            name: this.user.name,
                            email: this.user.email
                        },
                        body: this.$refs.body.value

                    });
                    this.$refs.body.value = "";
                    this.$refs.captcha.execute();
                }).catch(
                    error => {
                        this.$refs.captcha.execute(); // every time you submit, the reCAPTCHA token needs to be refreshed
                    });
            }
        }
    };
</script>
